package push.vo;

public class GcmSendFeedBackVo {
	
	private boolean result = false;				// 결과
	private String messageId;					// 
	private String errorCodeName;
	private String adminMsg;
	private String canonicalRegistrationId;
	
	public String getCanonicalRegistrationId() {
		return canonicalRegistrationId;
	}
	public void setCanonicalRegistrationId(String canonicalRegistrationId) {
		this.canonicalRegistrationId = canonicalRegistrationId;
	}
	public boolean isResult() {
		return result;
	}
	public void setResult(boolean result) {
		this.result = result;
	}
	public String getMessageId() {
		return messageId;
	}
	public void setMessageId(String messageId) {
		this.messageId = messageId;
	}
	public String getErrorCodeName() {
		return errorCodeName;
	}
	public void setErrorCodeName(String errorCodeName) {
		this.errorCodeName = errorCodeName;
	}
	public String getAdminMsg() {
		return adminMsg;
	}
	public void setAdminMsg(String adminMsg) {
		this.adminMsg = adminMsg;
	}
	
	@Override
	public String toString() {
		return "GcmSenderOutputVo [result=" + result + ", messageId="
				+ messageId + ", errorCodeName=" + errorCodeName
				+ ", adminMsg=" + adminMsg + ", canonicalRegistrationId="
				+ canonicalRegistrationId + "]";
	}


	
	
	
	
}
